#include <Uart_settings.h>

/*==================================================================================================
                                             CONSTANTS
==================================================================================================*/
static UART_PROPS properties =
{
   BEAR,         			// family
   9205,                	// platform
   1,                   	// uart_instance
   0x00000000,          	// gsbi_base
   0x078AF000,          	// uart_base
   0x01800000,          	// clock_base
   0,                       //0x01000000, 	 			// rts tlmm_base 
   0,                       //(0x3 << 16) | 0x0004,  	// rts: uart1 = (2ma, fcn 1, NP) Gpio : 3
   0,                       //0x01000000,  			// cts tlmm_base 
   0,                       //(0x2 << 16) | 0x0005,  	// cts: uart1 = (2ma, fcn 1, PD) Gpio : 2
   0x01000000,		   		// rx tlmm_base
   (0x1 << 16) | 0x0005,  	// rx:  uart1 = (2ma, fcn 1, PD) Gpio : 1
   0x01000000,		   		// tx tlmm_base
   (0x0 << 16) | 0x0004,  	// tx:  uart1 = (2ma, fcn 1, NP) Gpio : 0
   FALSE,                   //enable flow control
};


/*==================================================================================================
                                             FUNCTIONS
==================================================================================================*/
UART_PROPS* uart_get_properties(void)
{
   return &properties;
}
